package com.javaex.ex04;

public class Ex01 {
	
	public static void main(String[] args) {
		//초기화, 조건식, 증감식
		int i = 0;
		
		while(i<5) {
			System.out.println("I like Java"+i);
			
			i++;
		}
		
		//i=0 0<5 -->true 출력
		//i=1 1<5 -->true 출력
		//i=2 2<5 -->true 출력
		//i=3 3<5 -->true 출력
		//i=4 4<5 -->true 출력
		//i=5 5<5 -->false 
	
		
	}

}
