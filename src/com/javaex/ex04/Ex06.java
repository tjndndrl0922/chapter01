package com.javaex.ex04;

public class Ex06 {
	
	public static void main(String[] args) {
		
		for(int i=1;i<7;i++) {
			for(int j=1;j<7;j++) {
				System.out.print("*");
			}
			System.out.println("");
		}
		
		for(int w=0;w<=6;w++) {
			for(int s=0;s<w;s++) {
			System.out.print("*");
			}
			System.out.println("");
		}
		
		for(int q=1;q<10;q++) {
			for(int e=1;e<q;e++) {
				System.out.print("*");
			}
			System.out.println();
		}
	}

}
